package com.api.math;

public class MathDemo {
    public static void main(String[] args) {
        // 取绝对值：返回正数
        System.out.println(Math.abs(-121));
        // 向上取整
        System.out.println(Math.ceil(4.0000001));
        // 向下取整
        System.out.println(Math.floor(4.99999));
        // 求指数
        System.out.println(Math.pow(2.0, 16.0));
        // 四舍五入
        System.out.println(Math.round(4.3));
        System.out.println(Math.round(5.7));
        System.out.println(Math.round(4.991));

    }

}
